package BasicJava;

public class TempCelsiusToFaharenheit {
	public static void main(String[] args) {
		
		double temp = 561.33, f ;
		f = (temp*9/5)+32;
		
		System.out.println("Celsius into Far. "+f);
		
		double far = 235.4, c;
		c = (far-32)*5/9;
		
		System.out.println("far. into celsius "+c);
		
	}

}
