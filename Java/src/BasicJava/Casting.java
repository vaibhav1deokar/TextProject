package BasicJava;

public class Casting {
	public static void main(String[] args) {

		float b = 26;
		int a;
		a = (int) 4.9;

		System.out.println(a + " " + b);

		char e = '}';
		int f = e;

		System.out.println("Ascii value is " + f);

		int c = 115;
		char d = (char) c;

		System.out.println("Char is " + d);

	}

}
