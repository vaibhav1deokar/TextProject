package arrayobject;

public class Payment {

	int id;
	String p_mode;
	float balance;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getP_mode() {
		return p_mode;
	}

	public void setP_mode(String p_mode) {
		this.p_mode = p_mode;
	}

	public float getBalance() {
		return balance;
	}

	public void setBalance(float balance) {
		this.balance = balance;
	}
	
	

}
